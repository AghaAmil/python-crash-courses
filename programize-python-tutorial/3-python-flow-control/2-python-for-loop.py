# Loop Through a lists
languages = ['Python', 'Java', 'JavaScript', 'Go', 'Swift']

for language in languages:
    print('This is a programming language: ', language)

# blank line
print()

# Loop Through a String
sample_string = 'Amirhossein Moravveji'

for char in sample_string:
    print(char, end=' ')

# blank line
print('\n')

# Loop using Range() Function
for i in range(5):
    print(i, end=' ')

